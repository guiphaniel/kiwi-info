                    <p>On January 3, 2009, the first Bitcoin mining operation went underway, and a 50 BTC reward was sent to address <em>1A1zP1eP5QGefi2DMPTfTL5SLmv7DivfNa</em>. Although unconfirmed, it most likely went to the mysterious Satoshi Nakamoto, the inventor of Bitcoin. While no official transactions were recorded in the genesis block, the embedded text, “The Times 03/Jan/2009 Chancellor on brink of second bailout for banks” was a nod to the financial crisis of the time, and a major motivator behind the decentralized currency’s inception.</p>

                    <p>Fast forward to May 2021, and those 50 bitcoins would collect you a cool $2.9 million USD. However, due to either a quirk in the system or an intentional implementation in the code, the first 50 BTC can’t be spent. Instead, the first mining operation launched what would become global recognition of Satoshi Nakamoto’s vision of a decentralized currency, and become a financial and technical force to be reckoned with.</p>

                    <p>Today, there are at least 1 million unique “miners” for Bitcoin alone, not to mention all the other crypto currencies in existence. These miners are typically in the news for the wrong reasons: expending terawatt-hours of energy annually (.51% of the global electricity production) for mining purposes; gulping up GPUs on launch day despite the silicon shortage; and introducing novel, yet somewhat controversial, digital use cases such as CryptoKitties and non-fungible tokens (NFTs).</p>

                    <p>How is this all happening? What role is the miner playing? And how might this change the landscape of computing in the future?</p>

                    <p>Before diving into the details of crypto currency mining, it is worth understanding Satoshi’s original vision regarding the motivation of a cryptocurrency, and the use of the blockchain to address a set of very specific technical problems.</p>

                    <h3 id="Satoshis_Vision">Satoshi’s Vision</h3>

                    <p>According to the original whitepaper, Bitcoin is “a purely peer-to-peer version of electronic cash” aimed directly at modern financial institutions which act as middlemen for financial transactions. The primary issue regarding modern financial institutions, according to Satoshi Nakamoto, is the inherent trust required for all transactions.</p>

                    <p>This trust could potentially lead to <a href="">unidentifiable fraud</a> by the middleman, incur a high cost of mediation between <a href="">customers</a>, or involve complexity even with small, casual transactions as common as a <a href="">wire transfer</a>.</p>

                    <p>To address these issues, Satoshi described a distributed (i.e., not centralized) electronic payment system based on cryptographic proof instead of trust. Such a scheme, he argued, would make financial transactions immutable and “computationally impractical to reverse” and help protect against fraud. All this while also getting rid of the need for a trusted third party during transactions.</p>

                    <p>While great in theory, there was one minor technical challenge that needed ironing out. Namely, in a public ledger, anyone could claim a transaction even without the necessary funds. Who is there to arbitrate and assert that all transactions being performed are backed up by the appropriate amount of funds?</p>


                    <p>To make a decision, you should consider many of the following and perhaps <a href="">use a calculator</a>:</p>

                    <ul>
                        <li>hash rate of your rig</li>
                        <li>block reward</li>
                        <li>current mining difficulty</li>
                        <li>electricity cost</li>
                        <li>power consumption (W)</li>
                        <li>mining pool fees</li>
                        <li>cryptocurrency price</li>
                        <li>difficulty increase (rate of miners)</li>
                    </ul>

                    <p>The last two points are often the most variable, and are the hardest to predict. Then again, if profit is secondary and you are a believer in Satoshi’s vision of a truly distributed, peer-to-peer cash system, perhaps identifying the right coin and mining it is your calling. Regardless, it always makes sense to understand the technical details and differences of cryptocurrencies, in order to make an informed judgement of the currency being invested in.</p>

